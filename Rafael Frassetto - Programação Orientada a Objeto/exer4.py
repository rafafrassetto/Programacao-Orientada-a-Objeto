'''4) Escreva um algoritmo para ler uma temperatura em graus Fahrenheit,
calcular e escrever o valor correspondente em graus Celsius (baseado na
fórmula abaixo):
Observação: Para testar se a sua resposta está correta saiba que 100 ºC =
212F'''

a = float(input("Escreva a temperatura em graus Fahrenheit : "))

b = (a-32)/1.8

print('{}ºC'.format(b))


